
public class Ponto {

	private int x;
	private int y;
	
	// Construtor que recebe por parametro dois numeros inteiros x e y respectivamente.
	public Ponto(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	//Construtor que não recebe nada por parametro mas seus valores são inicializados em 0
	public Ponto() {
		this.x = 0;
		this.y = 0;
	}

}
